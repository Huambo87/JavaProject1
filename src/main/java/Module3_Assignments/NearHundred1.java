package Module3_Assignments;

public class NearHundred1 {
    public static void main(String[] args) {
        NearHundred1 nearHundred1 = new NearHundred1();
        nearHundred1.nearHundred(91);


    }

    public void nearHundred (int n) {

        if (n > 90 && n < 110) {

            System.out.println("Number " + n + " is 10 near 100 - " + " true" );

        }

        else {

            System.out.println("Number " + n + " is 10 near 100 - " + " false" );

        }





    }


}
